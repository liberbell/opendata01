# Python Comparison Operators

# TIPS:
# == --> is equal to
# <= --> is less than or equal to
# >= --> is greater than or equal to
# < --> is less than
# > --> is greater than



# < --> is less than
print(10<75)
print(75<10)

if 10 < 75:
    print('The bigger number is bigger')

# == --> is equal to
kitten = 10
tiger = 75

if kitten < tiger:
    print()

