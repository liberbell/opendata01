# Minimum and Maximum

playerOneScore = 10
playerTwoScore = 4

print(min(playerOneScore,playerTwoScore))

print(min(0, 4, 19, -5))
print(min('Kathryn', 'Katie'))
print(min('Angela', 'Bob'))

print(max(playerOneScore,playerTwoScore))

playerThreeScore = 21
print(max(playerOneScore,playerTwoScore,playerThreeScore))

print(max('Kathryn', 'Katie'))
